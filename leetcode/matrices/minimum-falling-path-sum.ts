function minFallingPathSum(matrix: number[][]): number {
	let n = matrix.length

	for (let i = 1; i < n; i++) {
		for (let j = 0; j < n; j++) {
			let topLeft = matrix[i - 1][j - 1] ?? Number.MAX_SAFE_INTEGER
			let middle = matrix[i - 1][j]
			let topRight = matrix[i - 1][j + 1] ?? Number.MAX_SAFE_INTEGER
			let current = matrix[i][j]
			matrix[i][j] = Math.min(topLeft + current, middle + current, topRight + current)
		}
	}

	return Math.min(...matrix[n - 1])
}

