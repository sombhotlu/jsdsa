function isObject(obj) {
	return obj !== null && typeof obj === 'object'
}

function objDiff(obj1, obj2) {
	let result = {}

	let [minimumKeyLengthObj, greaterKeyLengthObj] =
		Object.keys(obj1).length < Object.keys(obj2).length ? [obj1, obj2] : [obj2, obj1]

	Object.keys(minimumKeyLengthObj).forEach((key) => {
		if (
			(Array.isArray(minimumKeyLengthObj[key]) && !Array.isArray(greaterKeyLengthObj[key])) ||
			(!Array.isArray(minimumKeyLengthObj[key]) && Array.isArray(greaterKeyLengthObj[key]))
		) {
			result[key] = [obj1[key], obj2[key]]
		} else if (isObject(minimumKeyLengthObj[key]) && isObject(greaterKeyLengthObj[key])) {
			let val = objDiff(obj1[key], obj2[key])
			if (Object.keys(val).length) result[key] = val
		} else if (
			minimumKeyLengthObj[key] !== undefined &&
			greaterKeyLengthObj[key] !== undefined &&
			minimumKeyLengthObj[key] !== greaterKeyLengthObj[key]
		) {
			result[key] = [obj1[key], obj2[key]]
		}
	})
	return result
}

